package com.cafe24.chgs8411.headRequire.service;

public class HeadRequire {
	private int head_require_no;
	private String name;
	private String phone;
	private String require_title;
	private String require_content;
	private String require_date;
	
	public int getHead_require_no() {
		return head_require_no;
	}
	public void setHead_require_no(int head_require_no) {
		this.head_require_no = head_require_no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getRequire_title() {
		return require_title;
	}
	public void setRequire_title(String require_title) {
		this.require_title = require_title;
	}
	public String getRequire_content() {
		return require_content;
	}
	public void setRequire_content(String require_content) {
		this.require_content = require_content;
	}
	public String getRequire_date() {
		return require_date;
	}
	public void setRequire_date(String require_date) {
		this.require_date = require_date;
	}
}